function Landwirt.createMarker(type)
	if(isElement(Landwirt.marker))then destroyElement(Landwirt.marker)end
	if(isElement(Landwirt.blip))then destroyElement(Landwirt.blip)end
	if(type)then
		local rnd = math.random(1,#Landwirt["Marker"]);
		local tbl = Landwirt["Marker"][rnd];
		local x,y,z = tbl[1],tbl[2],tbl[3];
		Landwirt.marker = createMarker(x,y,z,"checkpoint",1.5,255,0,0);
		Landwirt.blip = createBlip(x,y,z,0,2,255,0,0);
		
		addEventHandler("onClientMarkerHit",Landwirt.marker,function(player)
			if(player == localPlayer)then
				triggerServerEvent("Landwirt.abgabe",localPlayer);
				Landwirt.createMarker("create");
			end
		end)
	end
end
addEvent("Landwirt.createMarker",true)
addEventHandler("Landwirt.createMarker",root,Landwirt.createMarker)